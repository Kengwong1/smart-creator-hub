    encoded = urllib.parse.quote(full_prompt)
    seed = int(time.time())
    # ‡πÄ‡∏û‡∏¥‡πà‡∏° quality=100 ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏Ç‡∏≠‡∏Ñ‡∏∏‡∏ì‡∏†‡∏≤‡∏û‡∏™‡∏π‡∏á‡∏™‡∏∏‡∏î
